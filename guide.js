  // بناء صفحة الفهرس: المجالات بألوانها وبنودها القابلة للنقر
  function buildIndex(book) {
    const byCat = {};
    book.pages.forEach((p, i) => { if (p.index) return; (byCat[p.cat] = byCat[p.cat] || []).push({ i, h: p.h }); });
    let html = '<h2 class="gd-h">📖 دليل حلالي — الفهرس</h2>'
      + '<div class="gd-blk"><span class="gd-lbl idea">الفكرة</span><div class="gd-txt">دليل واحد شامل مُصنّف بالمجالات. اضغط أي بند للانتقال إليه مباشرةً، أو استخدم «☰ فهرس» للبحث.</div></div>';
    CATS.forEach(c => {
      const items = byCat[c.key]; if (!items || !items.length) return;
      html += `<div class="gd-idx-cat" style="border-color:${c.color}">
          <div class="gd-idx-h" style="background:${c.color}">${c.emoji} ${esc(c.t)}</div>`
        + items.map(it => `<button class="gd-idx-item" data-goto="${it.i}"><span class="gd-idx-n" style="color:${c.color}">${it.i}</span><span class="gd-idx-t">${esc(it.h)}</span><span class="gd-idx-go">›</span></button>`).join('')
        + '</div>';
    });
    return html;
  }

