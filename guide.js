  /* ====== الفهرس الجانبي والبحث ====== */
  function pageRaw(i) { const p = state.book.pages[i]; return p.h + ' — ' + (p.index ? 'الفهرس' : stripTags(p.html)); }

  function snippet(raw, term) {
    if (term) { const idx = norm(raw).indexOf(norm(term)); if (idx >= 0) { const s = Math.max(0, idx - 28); return (s > 0 ? '…' : '') + raw.slice(s, idx + term.length + 56) + (raw.length > idx + term.length + 56 ? '…' : ''); } }
    return raw.slice(0, 95) + (raw.length > 95 ? '…' : '');
  }

  function toggleToc(show) {
    const panel = document.getElementById('gdTocPanel');
    if (!panel) return;
    panel.hidden = !show;
    if (show) { renderToc(''); const s = document.getElementById('gdTocSearch'); if (s) { s.value = ''; setTimeout(() => s.focus(), 30); } }
  }

  function renderToc(term) {
    const list = document.getElementById('gdTocList');
    if (!list) return;
    const t = (term || '').trim();
    const nt = norm(t);
    const rows = state.book.pages.map((p, i) => ({ i, h: p.h, cat: p.cat, raw: pageRaw(i) }))
      .filter(r => !nt || norm(r.h).includes(nt) || norm(r.raw).includes(nt));
    if (!rows.length) { list.innerHTML = `<div class="gd-toc-empty">لا توجد مواضيع مطابقة لـ «${esc(t)}»</div>`; return; }
    list.innerHTML = rows.map(r => {
      const c = CAT[r.cat] || CATS[0];
      return `<button class="gd-toc-item ${r.i === state.idx ? 'cur' : ''}" data-i="${r.i}">
          <span class="gd-toc-num" style="background:${c.color}">${r.i}</span>
          <span class="gd-toc-meta"><span class="gd-toc-h">${esc(r.h)}</span>
            <span class="gd-toc-snip">${esc(snippet(r.raw, t))}</span></span>
        </button>`;
    }).join('');
    list.querySelectorAll('.gd-toc-item').forEach(el =>
      el.addEventListener('click', () => { goTo(parseInt(el.dataset.i, 10)); toggleToc(false); }));
  }

  function goTo(i) {
    if (i == null || i < 0 || i >= state.book.pages.length) return;
    state.idx = i;
    paint();
  }

  function addSwipe(el) {
    if (!el) return;
    let x0 = null;
    el.addEventListener('touchstart', e => { x0 = e.touches[0].clientX; }, { passive: true });
    el.addEventListener('touchend', e => {
      if (x0 == null) return;
      const dx = e.changedTouches[0].clientX - x0; x0 = null;
      if (Math.abs(dx) < 45) return;
      turn(dx < 0 ? 1 : -1);   // RTL: لليسار = التالي، لليمين = السابق
    }, { passive: true });
  }

  function pageHtml(i) {
    const p = state.book.pages[i], b = state.book, n = b.pages.length;
    if (p.index) return `<div class="gd-paper-inner"><div class="gd-content gd-index">${buildIndex(b)}</div></div>`;
    const c = CAT[p.cat] || CATS[0];
    return `<div class="gd-paper-inner">
        <div class="gd-band" style="--tint:${c.color}"><span class="gd-band-emoji">${c.emoji}</span>`
      + `<span class="gd-band-title">${esc(c.t)}</span>`
      + `<span class="gd-band-num">${i} / ${n - 1}</span></div>
        <div class="gd-content">${p.html}</div>
      </div>`;
  }

  // ربط نقرات بنود الفهرس بعد رسم الصفحة
  function bindIndex(pageEl) {
    pageEl.querySelectorAll('[data-goto]').forEach(el =>
      el.addEventListener('click', () => goTo(parseInt(el.dataset.goto, 10))));
  }

  function paint() {
    const page = document.getElementById('gdPage');
    if (!page) return;
    page.innerHTML = pageHtml(state.idx);
    bindIndex(page);
    page.scrollTop = 0;
    const n = state.book.pages.length;
    const prog = document.getElementById('gdProg');
    if (prog) prog.textContent = state.idx === 0 ? 'الفهرس' : `${state.idx} / ${n - 1}`;
    const bar = document.getElementById('gdBar');
    if (bar) bar.style.width = (((state.idx + 1) / n) * 100) + '%';
    const prev = document.getElementById('gdPrev'), next = document.getElementById('gdNext');
    if (prev) prev.disabled = state.idx === 0;
    if (next) next.disabled = state.idx === n - 1;
    const dots = document.getElementById('gdDots');
    if (dots) dots.innerHTML = state.book.pages.map((_, i) =>
      `<span class="gd-dot ${i === state.idx ? 'on' : ''}" data-i="${i}"></span>`).join('');
    if (dots) dots.querySelectorAll('.gd-dot').forEach(d =>
      d.addEventListener('click', () => jump(parseInt(d.dataset.i, 10))));
  }

  function jump(i) {
    if (i === state.idx) return;
    turn(i > state.idx ? 1 : -1, i);
  }

  function turn(dir, target) {
    if (state.busy) return;
    const n = state.book.pages.length;
    const dest = (target != null) ? target : state.idx + dir;
    if (dest < 0 || dest >= n) return;
    const book = document.getElementById('gdBook');
    const current = document.getElementById('gdPage');
    if (!book || !current) { state.idx = dest; paint(); return; }
    state.busy = true;

    const leaf = document.createElement('div');
    leaf.className = 'gd-paper gd-leaf ' + (dir > 0 ? 'to-next' : 'to-prev');
    leaf.innerHTML = pageHtml(state.idx);
    current.innerHTML = pageHtml(dest);
    bindIndex(current);
    book.appendChild(leaf);
    void leaf.offsetWidth;
    leaf.classList.add('go');

    let finished = false;
    const done = (e) => {
      if (e && e.propertyName && e.propertyName !== 'transform') return;
      if (finished) return; finished = true;
      leaf.removeEventListener('transitionend', done);
      if (leaf.parentNode) leaf.parentNode.removeChild(leaf);
      state.idx = dest;
      state.busy = false;
      paint();
    };
    leaf.addEventListener('transitionend', done);
    setTimeout(() => done(), 800);
  }

  window.MrahiGuide = { render: render, GUIDE: GUIDE, CATS: CATS };
